CREATE TABLE public."tbEmpresa"
(
    id_empresa INTEGER GENERATED BY DEFAULT AS IDENTITY NOT NULL,
    str_nome TEXT COLLATE pg_catalog."default" NOT NULL,
    num_cnpj BIGINT NOT NULL,
    num_inscricao_estadual BIGINT,
    num_inscricao_municipal BIGINT,
    str_razao_social TEXT COLLATE pg_catalog."default",
    fg_status BIT(1) NOT NULL DEFAULT B'1'::"bit",
    CONSTRAINT "pk_id_empresa_tbEmpresa" PRIMARY KEY (id_empresa)
)
WITH (
    OIDS = FALSE
)
TABLESPACE pg_default;

ALTER TABLE public."tbEmpresa"
    OWNER to postgres;
